package SpaceInvaders.src.enemigos;

/**
 * Esta clase crea atributos que luego serán utilizados
 * @author grupo 4
 * @version 1.0
 */
public class Character {
    public int x;
    public int y;
    public int speed;
    /**
     * Método constructor
     */
    public Character(){
        /**
         * Método constructor
         * @param x posición
         * @param y posicisión
         * @param speed velocidad
         */
    }
    public Character(int x, int y ,int speed){
        this.x=x;
        this.y=y;
        this.speed=speed;

    }
}
